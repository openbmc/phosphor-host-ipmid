unsigned char find_sensor(uint8_t  sensor_number) {

    int i=0;

    while (g_sensortype[i][0] != 0xff) {
        if (g_sensortype[i][1] == sensor_number) {
            break;
        } else {
            i++;
        }

    }

    return g_sensortype[i][0];

}


